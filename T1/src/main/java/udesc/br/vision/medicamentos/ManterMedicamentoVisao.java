    public void apresentarMedicamentos(List<Medicamento> medicamentos){
        tabela = (DefaultTableModel) tblMedicamentos.getModel();
        tabela.setRowCount(0);

        for (Medicamento m : medicamentos){
            tabela.addRow(new Object[]{
                    m.getId(),
                    m.getNome(),
                    m.getEstoque(),
                    m.getValor(),
            });
        }
    }
